#include "libft.h"

size_t	ft_strlen3(const char *s, const char *c)
{
	size_t i;
	size_t j;

	i = 0;
	while (s[i] != 0)
	{
		j = 0;
		while (c[j])
		{
			if (c[j] == s[i])
				return (i);
			j++;
		}
		i++;
	}
	return (0);
}
