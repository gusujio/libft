#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *i;

	if (!(i = malloc(size + 1)))
		return (NULL);
	i[size] = '\0';
	while (size > 0)
	{
		i[size - 1] = 0;
		size--;
	}
	return (i);
}
